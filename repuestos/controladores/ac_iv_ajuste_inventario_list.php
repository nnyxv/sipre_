function buscar($frmBuscar) {
	$objResponse = new xajaxResponse();
	
	$valBusq = sprintf("%s|%s|%s|%s|%s|%s",
		$frmBuscar['lstEmpresa'],
		$frmBuscar['txtFechaDesde'],
		$frmBuscar['txtFechaHasta'],
		$frmBuscar['lstTipoMovimiento'],
		$frmBuscar['lstTipoVale'],
		$frmBuscar['txtCriterio']);
	
	$objResponse->loadCommands(listaVale(0, "CONCAT(CONVERT(id_vale, CHAR), CONVERT(tipo_documento, CHAR), numeracion_vale)", "DESC", $valBusq));
	
	return $objResponse;
}

