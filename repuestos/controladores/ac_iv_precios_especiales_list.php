		$htmlTh .= ordenarCampo("xajax_listaPreciosEspeciales", "30%", $pageNum, "nombre_cliente", $campOrd, $tpOrd, $valBusq, $maxRows, "Cliente");
		$htmlTh .= ordenarCampo("xajax_listaPreciosEspeciales", "10%", $pageNum, "credito", $campOrd, $tpOrd, $valBusq, $maxRows, "Tipo de Pago");
		$htmlTh .= ordenarCampo("xajax_listaPreciosEspeciales", "18%", $pageNum, "total_precios_especiales", $campOrd, $tpOrd, $valBusq, $maxRows, "Cant. Repuestos con Precio Especial");
